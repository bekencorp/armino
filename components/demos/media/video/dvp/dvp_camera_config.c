{
	bk_err_t ret = kNoErr;
	uint8_t addr = 0;
	uint8_t data = 0;
	uint8_t i2c_error_cnt = 0;
	for (uint32_t i = 0; i < size; i++) {
		addr = cfg_table[i][0];
		data = cfg_table[i][1];
		ret = camera_intf_sccb_write_byte(addr, data);
		if (ret != kNoErr) {
			i2c_error_cnt++;
		}

		if (i2c_error_cnt >= I2C_WRITE_FAILE_TIMES) {
			CAMERA_LOGE("I2C write failed!\r\n");
			break;
		}
	}
}

