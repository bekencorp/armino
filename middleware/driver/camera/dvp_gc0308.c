int gc0308_reset(void)
{
	SENSOR_I2C_WRITE(0xFE, 0x80);
	return 0;
}

int gc0308_dump(media_ppi_t ppi)
{
	uint32_t size, i;
	int ret = -1;
	uint8_t value = 0;

	LOGI("%s\n", __func__);

	size = sizeof(sensor_gc0308_init_talbe) / 2;

	for (i = 0; i < size; i++)
	{
		SENSOR_I2C_READ(sensor_gc0308_init_talbe[i][0], &value);
		LOGI("[0x%02x, 0x%02x]\r\n", sensor_gc0308_init_talbe[i][0], value);
	}

	switch (ppi)
	{
		case PPI_320X240:
		{
			size = sizeof(sensor_gc0308_QVGA_320_240_talbe) / 2;

			for (i = 0; i < size; i++)
			{
				SENSOR_I2C_READ(sensor_gc0308_QVGA_320_240_talbe[i][0], (uint8_t *)&value);
				LOGI("[%02x, %02x]\r\n", sensor_gc0308_QVGA_320_240_talbe[i][0], value);
			}

			ret = 0;
		}
		break;

		case PPI_640X480:
		{
			size = sizeof(sensor_gc0308_VGA_640_480_talbe) / 2;

			for (i = 0; i < size; i++)
			{
				SENSOR_I2C_READ(sensor_gc0308_VGA_640_480_talbe[i][0], &value);
				LOGI("[%02x, %02x]\r\n", sensor_gc0308_VGA_640_480_talbe[i][0], value);
			}

			ret = 0;
		}
		break;

		case PPI_800X600:
		case PPI_1280X720:
		default:
			break;

	}

	ret = kNoErr;

	return ret;

}

