int gc2145_reset(const dvp_camera_i2c_callback_t *cb)
{
	SENSOR_I2C_WRITE(0xFE, 0x80);
	return 0;
}

int gc2145_dump(const dvp_camera_i2c_callback_t *cb, media_ppi_t ppi)
{
	uint32_t size, i;
	int ret = -1;
	uint8_t value = 0;

	LOGI("%s\n", __func__);

	size = sizeof(sensor_gc2145_init_talbe) / 2;

	for (i = 0; i < size; i++)
	{
		SENSOR_I2C_READ(sensor_gc2145_init_talbe[i][0], &value);
		LOGI("[0x%02x, 0x%02x]\r\n", sensor_gc2145_init_talbe[i][0], value);
	}

	ret = kNoErr;

	return ret;

}

