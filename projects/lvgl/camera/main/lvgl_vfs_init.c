void lvgl_vfs_init(void)
{
	struct bk_little_fs_partition partition;
	char *fs_name = NULL;
	int ret;

	fs_name = "littlefs";
	partition.part_type = LFS_FLASH;
	partition.part_flash.start_addr = FLASH_START;
	partition.part_flash.size = FLASH_SIZE;
	

	bk_flash_set_protect_type(FLASH_PROTECT_NONE);
	ret = mkfs("PART_NONE", fs_name, &partition);
	if(ret < 0)
	{
		bk_printf("[%s][%d] format fail:%d\r\n", __FUNCTION__, __LINE__, ret);
		return ;
	}

	partition.mount_path = "/";
	ret = mount("SOURCE_NONE", partition.mount_path, fs_name, 0, &partition);
	if(ret < 0)
	{
		bk_printf("[%s][%d] mount fail:%d\r\n", __FUNCTION__, __LINE__, ret);
		return ;
	}
    else
    {
        bk_printf("[%s][%d] mount success:%d\r\n", __FUNCTION__, __LINE__, ret);
    }

    ret = mkdir("/dir1", O_RDWR | O_CREAT | O_APPEND);
    if(ret < 0)
	{
		bk_printf("[%s][%d] mkdir fail:%d\r\n", __FUNCTION__, __LINE__, ret);
		return ;
	}
    else
    {
        bk_printf("[%s][%d] mkdir success:%d\r\n", __FUNCTION__, __LINE__, ret);
    }

	int fd = open("/dir1/bg.jpg", O_RDWR | O_CREAT | O_APPEND);
	if(fd < 0)
	{
		bk_printf("[%s][%d] open bg.jpg fail:%d\r\n", __FUNCTION__, __LINE__, fd);
		return ;
	}

	write(fd, bg_jpg_data, sizeof(bg_jpg_data));
	close(fd);

	bk_flash_set_protect_type(FLASH_UNPROTECT_LAST_BLOCK);
	
	return ;
}
